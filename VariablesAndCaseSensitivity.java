public class VariablesAndCaseSensitivity {
    public static void main(String[] args) {
        
        // Declare an integer variable for student's age
        int studentAge = 15;

        // Declare a double variable for item price
        double itemPrice = 29.99;

        // Declare a string variable for student's name
        String studentName = "Alice";

        // Declare an integer variable 'counter'
        int counter = 10;

        // Declare another integer variable 'Counter' (different case)
        int Counter = 20;

        // Output the values
        System.out.println("Student Age: " + studentAge);
        System.out.println("Item Price: $" + itemPrice);
        System.out.println("Student Name: " + studentName);
        System.out.println("Counter: " + counter);
        System.out.println("Counter (Different Case): " + Counter);
    }
}